function buildEncodingArray(table: Record<number, number>): string[] {
  const encoding = new Array<string>(256).fill('')
  for (const key in table) {
    const code = Number(key)
    encoding[code] = String.fromCodePoint(table[code]!)
  }
  return encoding
}

/**
 * Returns the 256-entry byte -> Unicode string encoding for a standard-14
 * symbolic base font (/Symbol or /ZapfDingbats), or null for other fonts.
 * BaseFont subset prefixes ("ABCDEF+Symbol") are tolerated.
 */
export function symbolicBaseFontEncoding(baseFont: string): string[] | null {
  const name = baseFont.includes('+') ? baseFont.slice(baseFont.indexOf('+') + 1) : baseFont
  if (name === 'Symbol') return buildEncodingArray(SYMBOL_ENCODING)
  if (name === 'ZapfDingbats') return buildEncodingArray(ZAPF_DINGBATS_ENCODING)
  return null
}
