export function isUnicodeDecimalNumber(codePoint: number): boolean {
  let low = 0
  let high = DECIMAL_NUMBER_RANGES.length / 2 - 1
  while (low <= high) {
    const middle = (low + high) >>> 1
    const start = DECIMAL_NUMBER_RANGES[middle * 2]!
    const end = DECIMAL_NUMBER_RANGES[middle * 2 + 1]!
    if (codePoint < start) high = middle - 1
    else if (codePoint > end) low = middle + 1
    else return true
  }
  return false
}

/**
 * Default-ignorable characters hidden by shaping engines. Hangul fillers and
 * shorthand controls are deliberately visible for compatibility with fonts
 * that encode them as regular spacing glyphs.
 */
export function isDefaultIgnorable(codePoint: number): boolean {
  switch (codePoint >> 8) {
    case 0x00: return codePoint === 0x00AD
    case 0x03: return codePoint === 0x034F
    case 0x06: return codePoint === 0x061C
    case 0x17: return codePoint >= 0x17B4 && codePoint <= 0x17B5
    case 0x18: return codePoint >= 0x180B && codePoint <= 0x180E
    case 0x20: return (codePoint >= 0x200B && codePoint <= 0x200F)
      || (codePoint >= 0x202A && codePoint <= 0x202E)
      || (codePoint >= 0x2060 && codePoint <= 0x206F)
    case 0xFE: return (codePoint >= 0xFE00 && codePoint <= 0xFE0F) || codePoint === 0xFEFF
    case 0xFF: return codePoint >= 0xFFF0 && codePoint <= 0xFFF8
    default:
      return (codePoint >= 0x1D173 && codePoint <= 0x1D17A)
        || (codePoint >= 0xE0000 && codePoint <= 0xE0FFF)
  }
}
