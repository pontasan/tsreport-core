export function getContextLut(mode: ContextType): Uint8Array {
    return CONTEXT_LOOKUP_TABLE.subarray(mode << 9, (mode << 9) + 512);
}
// Compute context id (0-63) for a literal byte
export function getContext(p1: number, p2: number, lut: Uint8Array): number {
    return lut[p1]! | lut[256 + p2]!;
}
export function getContextInline(p1: number, p2: number, mode: ContextType): number {
    const offset = mode << 9;
    return CONTEXT_LOOKUP_TABLE[offset + p1]! | CONTEXT_LOOKUP_TABLE[offset + 256 + p2]!;
}
// Choose best context mode for a block of data
function validUtf8SequenceLength(data: Uint8Array, position: number, mask: number, remaining: number): number {
    const first = data[position & mask]!;
    if ((first & 0x80) === 0) return first === 0 ? 0 : 1;
    if (remaining > 1) {
        const second = data[(position + 1) & mask]!;
        if ((first & 0xe0) === 0xc0 && (second & 0xc0) === 0x80) {
            const symbol = ((first & 0x1f) << 6) | (second & 0x3f);
            if (symbol > 0x7f) return 2;
        }
        if (remaining > 2) {
            const third = data[(position + 2) & mask]!;
            if ((first & 0xf0) === 0xe0 && (second & 0xc0) === 0x80 && (third & 0xc0) === 0x80) {
                const symbol = ((first & 0x0f) << 12) | ((second & 0x3f) << 6) | (third & 0x3f);
                if (symbol > 0x7ff) return 3;
            }
            if (remaining > 3) {
                const fourth = data[(position + 3) & mask]!;
                if ((first & 0xf8) === 0xf0 && (second & 0xc0) === 0x80 &&
                    (third & 0xc0) === 0x80 && (fourth & 0xc0) === 0x80) {
                    const symbol = ((first & 0x07) << 18) | ((second & 0x3f) << 12) |
                        ((third & 0x3f) << 6) | (fourth & 0x3f);
                    if (symbol > 0xffff && symbol <= 0x10ffff) return 4;
                }
            }
        }
    }
    return 0;
}

export function isMostlyUtf8(data: Uint8Array, start: number, mask: number, length: number): boolean {
    let validByteCount = 0;
    let offset = 0;
    while (offset < length) {
        const sequenceLength = validUtf8SequenceLength(data, start + offset, mask, length - offset);
        if (sequenceLength === 0) offset++;
        else {
            validByteCount += sequenceLength;
            offset += sequenceLength;
        }
    }
    return validByteCount > 0.75 * length;
}

export function chooseContextMode(data: Uint8Array, start: number, mask: number, length: number, quality: number): ContextType {
    return quality >= 10 && !isMostlyUtf8(data, start, mask, length)
        ? ContextType.SIGNED
        : ContextType.UTF8;
}
